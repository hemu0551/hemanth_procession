        <h3>How do I change or cancel a hotel reservation?</h3>
        <div>
            <p>
              Some hotel bookings allow you to make changes to your reservation online in your itinerary. If you select Change Reservation on your itinerary and are redirected to this article, online changes are not available, and you will need to cancel and rebook your reservation.
            </p>
<p>
  You can make some changes to your existing hotel room reservation without a charge:
</p>
<ul>
  <li>Guest name</li>
  <li>Bed type</li>
  <li>Smoking preference</li>
  <li>Special requests</li>
  <li>Accessibility options</li>
</ul>
<p>
  You can also change the following, but a charge may apply:
</p>
<ul>
  <li>Room type</li>
  <li>Number of guests</li>
  <li>Travel dates</li>
</ul>
<p>
  <b>To make changes to your hotel reservation</b>
</p>
<ol>
  <li>Go to <a href="#">My Itineraries.</a></li>
  <li>Open the itinerary you want to change.</li>
  <li>Choose <b>Change Reservation</b> under <b>Manage Booking.</b></li>
  <li>Select <b>Edit</b> for the changes you wish to make.</li>
  <li>If the <b>Change Reservation</b> link sent you to this article, cancel your reservation and book it again.</li>
</ol>
<p>
  <b>To cancel your reservation online:</b>
</p>
<ol>
  <li>Go to <a href="#">My Itineraries.</a></li>
  <li>Open the itinerary you want to change.</li>
  <li>Click <b>Cancel Room.</b></li>
  <li>Review the refund and fee details before finalizing the cancellation.</li>
</ol>
<p>
  Refunds are issued within 7-10 days, but your bank or credit card company may not show a credit on your account until the end of the next billing cycle.
</p>
<p>
  <b>Good to know</b>
</p>
<ul>
  <li>If you need assistance with your hotel reservation, please call Expedia Customer Support at 1860 500 2121. Calls to this number cost the same as calls to local rate numbers.</li>
  <li>To change your reservation to a different hotel, cancel the original reservation and rebook with the hotel of your choice.</li>
  <li>If you change your accessibility options or make special requests, call the hotel to confirm that your changes have been made.</li>
  <li>If your booking is marked as non-refundable, the hotel will not allow any changes to the original reservation.</li>
  <li>To change or cancel Pay Later or partial deposit bookings after the hotel's cancellation deadline, contact the hotel directly.</li>
  <li>If you do not see Cancel Room for your itinerary, the hotel's cancellation deadline has passed and you can no longer cancel this reservation.</li>
  <li>While Expedia does not charge fees for changes or cancellations, we are required to pass along the hotel's non-refundable rate plans or fees. Such fees are highest when cancellations occur within 48 hours of your scheduled travel dates.</li>
</ul>
